package ToyFactor_Collection;

public class Toy {
	
	private String name; 
	private int age;
	private int prace;
	private String color;
	
	public Toy() {}

	public Toy(String name, int age, int prace, String color) {
		super();
		this.name = name;
		this.age = age;
		this.prace = prace;
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getPrace() {
		return prace;
	}

	public void setPrace(int prace) {
		this.prace = prace;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}